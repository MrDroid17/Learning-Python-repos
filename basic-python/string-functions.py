
theString = 'my Name is Kumar'
# Capitalize
print('capitalize -', theString.capitalize())

# Swap case
print('swap-case -', theString.swapcase())

# Get length
print('string length -', len(theString))

# Replace
print('string replace -', theString.replace('Kumar','Choudhary'))

# Count sub string
print('string sub-string -', theString.count('u'))

# Starts with
print('starts-with -', theString.startswith('my'))

# Ends with
print('ends-with -', theString.endswith('Kumar'))

# Split to list
print('split to list -', theString.split())

# find
print('find -', theString.find('K'))

# is all alphanumeric
print('is alphanumeric -', theString.isalnum() )

# is all alphabetic
print('is alphabetic -', theString.isalpha() )

# is all numeric
print('is numeric -', theString.isnumeric())





